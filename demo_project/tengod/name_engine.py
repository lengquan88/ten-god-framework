@dataclass
class WugeResult:
    """五格结果"""
    tian_ge: int       # 天格
    ren_ge: int        # 人格
    di_ge: int         # 地格
    wai_ge: int        # 外格
    zong_ge: int       # 总格
    tian_ge_ji: str    # 天格吉凶
    ren_ge_ji: str     # 人格吉凶
    di_ge_ji: str      # 地格吉凶
    wai_ge_ji: str     # 外格吉凶
    zong_ge_ji: str    # 总格吉凶


@dataclass
class NameAnalysis:
    """姓名学分析结果"""
    surname: str       # 姓氏
    given_name: str    # 名字
    surname_strokes: int
    given_strokes: List[int]
    
    # 五格
    wuge: WugeResult
    
    # 三才
    sancai: Tuple[str, str, str]  # 天人地
    sancai_ji: str
    sancai_desc: str
    
    # 总体评分
    overall_score: float  # 0-100
    overall_grade: str    # 优/良/中/差
    
    # 建议
    suggestions: List[str] = field(default_factory=list)


# ============================================================================
# 核心引擎
# ============================================================================

class NameEngine:
    """姓名学分析引擎"""

    @classmethod
    def _get_stroke(cls, char: str) -> int:
        """获取康熙字典笔画数"""
        return KANGXI_STROKES.get(char, len(char) * 2 + 4)  # 默认估算

    @classmethod
    def _get_shuli_jixiong(cls, num: int) -> Tuple[str, str]:
        """获取数理吉凶"""
        if num > 81:
            num = num % 81 or 81
        return _SHU_LI_JI_XIONG.get(num, ("凶", "数理不详"))

    @classmethod
    def _get_bihua_wuxing(cls, strokes: int) -> str:
        """获取笔画五行"""
        if strokes > 81:
            strokes = strokes % 81 or 81
        return BIHUA_WUXING.get(strokes, "土")

    @classmethod
    def _calc_wuge(cls, surname_strokes: int, given_strokes: List[int]) -> WugeResult:
        """计算五格"""
        g1 = given_strokes[0] if len(given_strokes) > 0 else 0
        g2 = given_strokes[1] if len(given_strokes) > 1 else 0
        
        tian = surname_strokes + 1  # 天格（单姓+1）
        ren = surname_strokes + g1   # 人格
        di = g1 + g2                 # 地格
        zong = surname_strokes + g1 + g2  # 总格
        wai = zong - ren + 1         # 外格
        
        tian_ji, _ = cls._get_shuli_jixiong(tian)
        ren_ji, _ = cls._get_shuli_jixiong(ren)
        di_ji, _ = cls._get_shuli_jixiong(di)
        zong_ji, _ = cls._get_shuli_jixiong(zong)
        wai_ji, _ = cls._get_shuli_jixiong(wai)
        
        return WugeResult(
            tian_ge=tian, ren_ge=ren, di_ge=di, wai_ge=wai, zong_ge=zong,
            tian_ge_ji=tian_ji, ren_ge_ji=ren_ji, di_ge_ji=di_ji,
            wai_ge_ji=wai_ji, zong_ge_ji=zong_ji,
        )

    @classmethod
    def _calc_sancai(cls, wuge: WugeResult) -> Tuple[str, str, str]:
        """计算三才（天人地）"""
        tian_wx = cls._get_bihua_wuxing(wuge.tian_ge)
        ren_wx = cls._get_bihua_wuxing(wuge.ren_ge)
        di_wx = cls._get_bihua_wuxing(wuge.di_ge)
        return (tian_wx, ren_wx, di_wx)

    @classmethod
    def analyze(cls, surname: str, given_name: str) -> NameAnalysis:
        """
        完整姓名学分析
        
        参数:
            surname: 姓氏（单姓）
            given_name: 名字（1-2个字）
        """
        surname_strokes = cls._get_stroke(surname)
        given_strokes = [cls._get_stroke(c) for c in given_name]
        
        # 五格
        wuge = cls._calc_wuge(surname_strokes, given_strokes)
        
        # 三才
        sancai = cls._calc_sancai(wuge)
        sancai_ji, sancai_desc = SANCAI_JIXIONG.get(sancai, ("半吉", "三才配置一般"))
        
        # 评分
        ji_map = {"大吉": 100, "吉": 75, "半吉": 50, "凶": 25}
        scores = []
        for g in [wuge.tian_ge_ji, wuge.ren_ge_ji, wuge.di_ge_ji, wuge.wai_ge_ji, wuge.zong_ge_ji]:
            scores.append(ji_map.get(g, 25))
        sancai_score = 100 if sancai_ji == "大吉" else (75 if sancai_ji == "吉" else 40)
        
        overall = sum(scores) / 5 * 0.6 + sancai_score * 0.4
        
        if overall >= 80:
            grade = "优"
        elif overall >= 60:
            grade = "良"
        elif overall >= 40:
            grade = "中"
        else:
            grade = "差"
        
        # 建议
        suggestions = []
        if wuge.ren_ge_ji == "凶":
            suggestions.append("人格数理欠佳，建议选择笔画更佳的名字")
        if wuge.di_ge_ji == "凶":
            suggestions.append("地格数理欠佳，需注意名字的笔画搭配")
        if sancai_ji == "凶":
            suggestions.append("三才配置不吉，建议调整名字的五行属性")
        if not suggestions:
            suggestions.append("姓名整体配置良好，五格数理吉多凶少")
        
        return NameAnalysis(
            surname=surname,
            given_name=given_name,
            surname_strokes=surname_strokes,
            given_strokes=given_strokes,
            wuge=wuge,
            sancai=sancai,
            sancai_ji=sancai_ji,
            sancai_desc=sancai_desc,
            overall_score=round(overall, 1),
            overall_grade=grade,
            suggestions=suggestions,
        )

    @classmethod
    def format_text(cls, result: NameAnalysis) -> str:
        """格式化输出"""
        g = result.wuge
        lines = []
        lines.append("╔══════════════════════════════════════╗")
        lines.append("║       姓 名 学 分 析                ║")
        lines.append("╠══════════════════════════════════════╣")
        lines.append(f"║ 姓名: {result.surname}{result.given_name}")
        lines.append(f"║ 姓氏笔画: {result.surname_strokes}  名字笔画: {result.given_strokes}")
        lines.append("╠══════════════════════════════════════╣")
        lines.append(f"║ 天格: {g.tian_ge:2d}  [{g.tian_ge_ji}]")
        lines.append(f"║ 人格: {g.ren_ge:2d}  [{g.ren_ge_ji}]  ← 主运")
        lines.append(f"║ 地格: {g.di_ge:2d}  [{g.di_ge_ji}]  ← 前运")
        lines.append(f"║ 外格: {g.wai_ge:2d}  [{g.wai_ge_ji}]  ← 副运")
        lines.append(f"║ 总格: {g.zong_ge:2d}  [{g.zong_ge_ji}]  ← 后运")
        lines.append("╠══════════════════════════════════════╣")
        lines.append(f"║ 三才: {'·'.join(result.sancai)}  [{result.sancai_ji}]")
        lines.append(f"║ 说明: {result.sancai_desc}")
        lines.append("╠══════════════════════════════════════╣")
        lines.append(f"║ 综合评分: {result.overall_score}/100  [{result.overall_grade}]")
        lines.append(f"║ 建议: {result.suggestions[0] if result.suggestions else ''}")
        lines.append("╚══════════════════════════════════════╝")
        return "\n".join(lines)


